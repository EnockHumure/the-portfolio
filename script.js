document.addEventListener('DOMContentLoaded', () => {
    // Custom Cursor Logic - RELIABLE VERSION
    const dot = document.querySelector('.cursor-dot');
    const ring = document.querySelector('.cursor-ring');

    let mouseX = 0;
    let mouseY = 0;
    let ringX = 0;
    let ringY = 0;

    window.addEventListener('mousemove', (e) => {
        mouseX = e.clientX;
        mouseY = e.clientY;
        dot.style.left = `${mouseX}px`;
        dot.style.top = `${mouseY}px`;
    });

    function animateCursor() {
        const lerp = 0.1;
        ringX += (mouseX - ringX) * lerp;
        ringY += (mouseY - ringY) * lerp;
        ring.style.left = `${ringX}px`;
        ring.style.top = `${ringY}px`;
        requestAnimationFrame(animateCursor);
    }
    animateCursor();

    const interactives = document.querySelectorAll('a, button, input, textarea, .bento-card, .lang-btn, .theme-toggle');
    interactives.forEach(el => {
        el.addEventListener('mouseenter', () => ring.classList.add('hover'));
        el.addEventListener('mouseleave', () => ring.classList.remove('hover'));
    });

    // Theme Switching Logic
    const themeToggle = document.getElementById('theme-toggle');
    const body = document.body;
    let currentTheme = localStorage.getItem('portfolioTheme') || 'dark';

    function setTheme(theme) {
        body.setAttribute('data-theme', theme);
        const icon = themeToggle.querySelector('i');
        if (theme === 'light') {
            icon.classList.replace('fa-moon', 'fa-sun');
        } else {
            icon.classList.replace('fa-sun', 'fa-moon');
        }
        localStorage.setItem('portfolioTheme', theme);
    }

    themeToggle.addEventListener('click', () => {
        const newTheme = body.getAttribute('data-theme') === 'dark' ? 'light' : 'dark';
        setTheme(newTheme);
    });

    setTheme(currentTheme);

    // Profile Image Tilt Effect
    const tiltContainer = document.querySelector('.img-tilt-container');
    const imgLayer = document.querySelector('.img-layer');

    if (tiltContainer && imgLayer) {
        tiltContainer.addEventListener('mousemove', (e) => {
            const rect = tiltContainer.getBoundingClientRect();
            const x = e.clientX - rect.left;
            const y = e.clientY - rect.top;
            const centerX = rect.width / 2;
            const centerY = rect.height / 2;
            const rotateX = (y - centerY) / 15;
            const rotateY = (centerX - x) / 15;
            imgLayer.style.transform = `rotateX(${rotateX}deg) rotateY(${rotateY}deg) scale(1.05)`;
        });

        tiltContainer.addEventListener('mouseleave', () => {
            imgLayer.style.transform = `rotateX(0) rotateY(0) scale(1)`;
        });
    }

    // Language Switching Logic - BULLETPROOF VERSION
    const langBtns = document.querySelectorAll('.lang-btn');
    let currentLang = localStorage.getItem('preferredLang') || 'en';

    function updateContent(lang) {
        const t = translations[lang];
        if (!t) return;

        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (t[key]) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    el.placeholder = t[key];
                } else {
                    el.innerText = t[key];
                }
            }
        });

        document.querySelectorAll('[data-i18n-span]').forEach(el => {
            const key = el.getAttribute('data-i18n-span');
            if (t[key]) el.innerText = t[key];
        });

        langBtns.forEach(btn => {
            if (btn.getAttribute('data-lang') === lang) {
                btn.classList.add('active');
            } else {
                btn.classList.remove('active');
            }
        });

        localStorage.setItem('preferredLang', lang);
    }

    langBtns.forEach(btn => {
        btn.addEventListener('click', function(e) {
            e.preventDefault();
            updateContent(this.getAttribute('data-lang'));
        });
    });

    updateContent(currentLang);

    // Terminal Blink Effect
    const blink = document.getElementById('cursor-blink');
    if (blink) {
        setInterval(() => {
            blink.style.opacity = blink.style.opacity === '0' ? '1' : '0';
        }, 500);
    }

    // Reveal Animations
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('reveal-visible');
            }
        });
    }, { threshold: 0.1 });

    document.querySelectorAll('.bento-card, .hero-text, .hero-visual, .contact-wrapper, .education, .section-title').forEach(el => {
        el.classList.add('reveal-hidden');
        observer.observe(el);
    });

    // Smooth Scroll
    document.querySelectorAll('nav a').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            const targetId = this.getAttribute('href');
            if (targetId.startsWith('#')) {
                e.preventDefault();
                const targetSection = document.querySelector(targetId);
                if (targetSection) {
                    window.scrollTo({
                        top: targetSection.offsetTop - 80,
                        behavior: 'smooth'
                    });
                }
            }
        });
    });
});

const revealStyle = document.createElement('style');
revealStyle.textContent = `
    .reveal-hidden { opacity: 0; transform: translateY(30px); transition: all 0.8s cubic-bezier(0.2, 0.8, 0.2, 1); }
    .reveal-visible { opacity: 1; transform: translateY(0); }
`;
document.head.appendChild(revealStyle);
